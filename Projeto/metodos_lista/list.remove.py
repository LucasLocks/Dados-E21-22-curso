#list.remove.py

""" list.remove = O remove()método remove o primeiro elemento correspondente
(que é passado como argumento) da lista. """

numeros = [2, 3, 5, 7, 9, 11]

# remove 9 da lista
numeros.remove(9)

# Lista numeros atualizada
print('Lista atualizada: ', numeros)
# Resultado: Lista atualizada: [2, 3, 5, 7, 11]
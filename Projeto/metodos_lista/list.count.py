""" list.count() = O count()método retorna o número de vezes que o elemento
especificado no () aparece na lista. """

numeros = [2, 3, 5, 2, 11, 2, 7]

# verifica quantas vezes o 2 aparece na lista
verifica = numeros.count(2)


print('O numero 2 aparece:', verifica)

# Resultado: O numero 2 aparece: 3